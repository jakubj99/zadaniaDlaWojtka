package pl.wsb;

public class Car {

    String manufacture;
    String model;
    Double engineCapacity;
    int horsePower;

    public Car(String manufacture, String model, Double engineCapacity, int horsePower) {
        this.manufacture = manufacture;
        this.model = model;
        this.engineCapacity = engineCapacity;
        this.horsePower = horsePower;
    }
}
